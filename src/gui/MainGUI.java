    private void newVehicleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newVehicleButtonActionPerformed
        vehicle = new Vehicle();
        if (!(newCubicValue.getText().equals("")) && !(newPowerValue.getText().equals(""))) {
            vehicle.setMake(newMakeValue.getSelectedItem().toString());
            vehicle.setModel(newModelValue.getText());
            vehicle.setRegistration_no(newRegistrationValue.getText());
            vehicle.setProduction_month((Date) newProductDateValue.getModel().getValue());
            vehicle.setBody(newBodyValue.getSelectedItem().toString());
            vehicle.setFuel_type(newFuelTypeValue.getSelectedItem().toString());
            vehicle.setCubic_vol(Integer.parseInt(newCubicValue.getText()));
            vehicle.setPower(Integer.parseInt(newPowerValue.getText()));
            vehicle.setEngine_code(newEngineCodeValue.getText());
            vehicle.setOwner(owner);
            vehicle.setLogo(new LogoDAO().getLogoByBrand(newMakeValue.getSelectedItem().toString()));
            vehicle = (Vehicle) new VehicleDAO().saveVehicle(vehicle);
            loadVehicleBox.addItem(vehicle);
            JOptionPane.showMessageDialog(null, bundle.getString("vehicleNewMessage"));
            if (vehicle != null) {
                logoVehicleLabel.setIcon(new ImageIcon(vehicle.getLogo().getLogo()));
                makeLabel.setText(vehicle.getMake());
                modelLabel.setText(vehicle.getModel());
                registrationLabel.setText(vehicle.getRegistration_no());
                DateFormat df = new SimpleDateFormat("MM/yyyy");
                String dateProduced = df.format(vehicle.getProduction_month());
                productionMonthLabel.setText(dateProduced);
                bodyLabel.setText(vehicle.getBody());
                fuelLabel.setText(vehicle.getFuel_type());
                engineSizeLabel.setText(Integer.toString(vehicle.getCubic_vol()));
                enginePowerLabel.setText(Integer.toString(vehicle.getPower()));
                engineCodeLabel.setText(vehicle.getEngine_code());
                ownerNameLabel.setText(vehicle.getOwner().getFull_name());
                newVehicleDialog.dispose();
            }
            refreshServiceTable();
        } else {
            JOptionPane.showMessageDialog(null, bundle.getString("engineSizeNotNullMessage"));
        }
        

    }//GEN-LAST:event_newVehicleButtonActionPerformed

