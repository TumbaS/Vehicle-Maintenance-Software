    public void refreshServiceTable() {
        DefaultTableModel model = (DefaultTableModel) serviceTable.getModel();
        model.setRowCount(0);
        if (vehicle != null) {
            vehicle = (Vehicle) new VehicleDAO().getVehicle(vehicle.getVehicle_id());
            
            if (isFilterByPart.isSelected()) {
                List<Service> servicesList = (List<Service>) new ServiceDAO().getServicesByPart((Part) filterByPartBox.getSelectedItem(), vehicle);
                DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                if (servicesList != null) {
                    for (Service service : servicesList) {
                        int data0 = service.getService_id();
                        String data1 = df.format(service.getService_date());
                        int data2 = service.getOdometer_value();
                        String data3 = service.getService_name();
                        Part data4 = service.getPart();
                        String data5 = service.getService_description();
                        
                        Object[] row = {data0, data1, data2, data3, data4, data5};
                        model.addRow(row);
                    }
                }
                
            } else {
                Set<Service> services = vehicle.getServices();
                DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
                for (Service service : services) {
                    int data0 = service.getService_id();
                    String data1 = df.format(service.getService_date());
                    int data2 = service.getOdometer_value();
                    String data3 = service.getService_name();
                    Part data4 = service.getPart();
                    String data5 = service.getService_description();
                    
                    Object[] row = {data0, data1, data2, data3, data4, data5};
                    model.addRow(row);
                }
            }
        }
    }
    
    public void refreshComboBoxes(Vehicle vehicle) {
        allowEvent = false;
        Part noPart = null;
        if (vehicle != null) {
            newServicePartValue.removeAllItems();
            editPartBox.removeAllItems();
            editServicePartValue.removeAllItems();
            filterByPartBox.removeAllItems();
            filterByPartBox.addItem(noPart);
            parts = vehicle.getParts();
            if (parts != null) {
                for (Part part : parts) {
                    newServicePartValue.addItem(part);
                    editPartBox.addItem(part);
                    editServicePartValue.addItem(part);
                    filterByPartBox.addItem(part);
                }
            }
            allowEvent = true;
        }
    }

